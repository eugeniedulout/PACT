package com.example.testmenu.algorithmie.dijkstra;

public class PreviousAndPi {
	private Previous previous;
	private Pi pi;
	public PreviousAndPi(Previous previous, Pi pi) {
		this.previous = previous;
		this.pi = pi;
	}
	public Previous getPrevious() {
		return previous;
	}
	public Pi getPI() {
		return pi;
	}

	

}
