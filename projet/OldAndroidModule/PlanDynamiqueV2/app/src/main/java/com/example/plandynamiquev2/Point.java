package com.example.plandynamiquev2;

public class Point {
    private int x ;
    private int y ;

    public int getAbscisse(){
        return this.x;
    }

    public int getOrdonnee(){
        return this.y;
    }


    public Point(int x, int y){
        this.x = x ;
        this.y = y ;

    }
}
